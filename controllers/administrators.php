class Administrators extends ClearOS_Controller
{
    /**
     * Administrators view.
     *
     * @return view
     */

    function index()
    {
        // Show account status widget if we're not in a happy state
        //---------------------------------------------------------

        $this->load->module('accounts/status');

        if ($this->status->unhappy()) {
            $this->status->widget('administrators');
            return;
        }

        // Load libraries
        //---------------

        $this->lang->load('administrators');

        // Load views
        //-----------

        $views = array('administrators/settings', 'administrators/policy');

        $this->page->view_forms($views, lang('administrators_app_name'));
    }
}
